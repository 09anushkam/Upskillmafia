const grade="C";
switch(grade){
    case "A":
        console.log("A->very good");
        break;
    case "B":
        console.log("B->Good,Keep Learning");
        break;
    case "C":
        console.log("C->Need Improvement");
        break;
    case "D":
        console.log("D->Not Good");
        break;
    default:
        console.log("Invalid Grade");
}